package com.example.casa.lifecare.entidades;

public class Risco {
    private Integer id;
    private String tipo;

    public Risco() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }



    public Risco(Integer id, String tipo) {
        this.id = id;
        this.tipo = tipo;





    }
}
